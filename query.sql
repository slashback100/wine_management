--config
create table    country (id int AUTO_INCREMENT PRIMARY KEY, label varchar(20));
create table    color   (id int  AUTO_INCREMENT PRIMARY KEY, label varchar(20));
create table    to_keep (id int  AUTO_INCREMENT PRIMARY KEY, label varchar(20));
create table    size    (id int  AUTO_INCREMENT PRIMARY KEY, label varchar(20), volume varchar(20));
create table    cepage  (id int AUTO_INCREMENT PRIMARY KEY, label varchar(20));
create table    region  (id int  AUTO_INCREMENT PRIMARY KEY, country_id int, label varchar(50), FOREIGN KEY (country_id) REFERENCES country(id));
create table    category    (id int AUTO_INCREMENT PRIMARY KEY, label varchar(20));
create table    apellation  (id int AUTO_INCREMENT PRIMARY KEY, label varchar(200), region_id int, sub_division varchar(20), FOREIGN KEY (region_id) REFERENCES region(id));
create table    millesime   (id int AUTO_INCREMENT PRIMARY KEY, year int, region_id int, color_id int, note int, category_id int, apellation_id int, priority int, FOREIGN KEY (apellation_id) REFERENCES apellation(id), FOREIGN KEY (category_id) REFERENCES category(id), FOREIGN KEY (color_id) REFERENCES color(id), FOREIGN KEY (region_id) REFERENCES region(id));
--data
create table    wine    (id int  AUTO_INCREMENT PRIMARY KEY, name_climat varchar(50), year int, millesime_id int, propriete varchar(20), producer varchar(20), country_id int, region_id int, apellation_id int, category_id int, color_id int, date_optimum int, end_optimum int, to_keep_id int, distinction varchar(20), medal varchar(20), view varchar(200), noze varchar(200), taste varchar(200), my_note int, meal varchar(200), comment varchar(200), foreign key (millesime_id) references millesime(id), foreign key (country_id) references country(id), foreign key (region_id) references region(id), foreign key (apellation_id) references apellation(id), foreign key (category_id) references category(id), foreign key (color_id) references color(id), foreign key (to_keep_id) references to_keep(id));
create table    position_cave   (id int  AUTO_INCREMENT PRIMARY KEY, wine_id int, quantity int, size_id int, wall varchar(6), casier varchar(6), foreign key (wine_id) references wine(id));
create table    wine_cepage (id int AUTO_INCREMENT PRIMARY KEY, wine_id int, cepage_id int, percentage int, FOREIGN KEY (wine_id) REFERENCES wine(id), FOREIGN KEY (cepage_id) REFERENCES cepage(id));
create table    stock   (id int  AUTO_INCREMENT PRIMARY KEY, wine_id int, size_id int, quantity int, FOREIGN KEY (wine_id) REFERENCES wine(id), FOREIGN KEY (size_id) REFERENCES size(id));

create table    buy (id int  AUTO_INCREMENT PRIMARY KEY, wine_id int, size_id int, location varchar(50), buy_date date, quantity int, unit_price decimal(5,2), FOREIGN KEY (wine_id) REFERENCES wine(id), FOREIGN KEY (size_id) REFERENCES size(id));
create table    out_record  (id int AUTO_INCREMENT PRIMARY KEY, wine_id int, size_id int, quantity int, out_date date, foreign key (size_id) references size(id), foreign key (wine_id) references wine(id));
create table    casier_capacity (id int auto_increment primary key, wall varchar(6), casier varchar(6), capacity int);

create view stock_sub as select quantity, size_id, wine_id from buy union all select -quantity, size_id, wine_id from out_record;
create view stock_view as select sum(quantity) quantity, size_id, wine_id from stock_sub t group by t.size_id, t.wine_id;

